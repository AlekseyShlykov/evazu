import { readdir, writeFile } from 'fs/promises';
import { join, extname, basename } from 'path';

/**
 * Scans `public/images` and emits `lib/generatedWebpManifest.ts` listing every
 * `.webp` file that actually exists. The runtime helper `webpSrcFor` consults
 * this list before pointing `<picture>` `<source>` at a `.webp` companion, so
 * we never advertise a WebP URL that would 404 in the browser.
 *
 * The optimize-images script intentionally skips writing a `.webp` when it
 * would be *larger* than the source (e.g. Yerevan1.jpg). Without this
 * manifest, those images would render as broken on any browser that supports
 * WebP (i.e. all modern browsers), because the browser commits to the chosen
 * `<source>` and does not fall back to `<img>` on a network failure.
 */

const ROOT = new URL('..', import.meta.url);
const IMAGES_DIR = new URL('public/images/', ROOT);
const OUTPUT = new URL('lib/generatedWebpManifest.ts', ROOT);

async function main() {
  const entries = await readdir(IMAGES_DIR);
  const webpFiles = entries
    .filter((f) => extname(f).toLowerCase() === '.webp')
    .map((f) => basename(f))
    .sort((a, b) => a.localeCompare(b));

  const header =
    '// AUTO-GENERATED by scripts/generate-webp-manifest.mjs. Do not edit by hand.\n' +
    '// Lists `.webp` companions that actually exist under `public/images/`.\n\n';

  const body =
    'export const webpManifest: ReadonlySet<string> = new Set([\n' +
    webpFiles.map((name) => `  ${JSON.stringify(name)},`).join('\n') +
    (webpFiles.length ? '\n' : '') +
    ']);\n';

  await writeFile(OUTPUT, header + body, 'utf8');
  console.log(`generate-webp-manifest: ${webpFiles.length} .webp companions`);
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
